    private void okButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okButtonActionPerformed
        // TODO add your handling code here:
        String choix = "";
        if(consulterRadioButton.isSelected())
            choix = "Consulter";
        else if(imprimerRadioButton.isSelected())
        {
            choix = "Imprimer";
            ThreadImprimer imp = new ThreadImprimer(prop.getProperty("date.format"));
            imp.start();
        }
        else if(encaisserRadioButton.isSelected())
        {
            choix = "Encaisser";
            setEncaisser(true);
            setAddition(tableEncaisser.getAddition());
            
            tableEncaisser.setNbCouvertsEnCours(0);
            tableEncaisser.setPaye(true);
            tableEncaisser.setPrixBoissons(0);
            tableEncaisser.setAddition(0);
            tableEncaisser.getCommandesPlat().clear();
            tableEncaisser.getPlatServi().clear();
        }
        
        System.out.println("Option choisie: " + choix);
        
        dispose();
    }//GEN-LAST:event_okButtonActionPerformed


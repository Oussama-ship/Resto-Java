    public EncaisserFrame(java.awt.Frame parent, boolean modal, Table t) {
        super(parent, modal);
        
        prop = new Properties();
        String outfile = System.getProperty("user.dir") + "\\config.properties";
        File monFile = new File(outfile);
        try
        {
            prop.load (new FileInputStream (monFile));
        }
        catch (FileNotFoundException e) { System.out.println("Fichier de propriétés non trouvé !"); }
        catch (IOException e) { System.out.println("Aie : " + e.getMessage()); }
        
        setTitle("Restaurant \"" + prop.getProperty("nomRestaurant"));
        initComponents();
        table.setText(t.getNumTable());
        nbCouvert.setText(String.valueOf(t.getNbCouvertsEnCours()));
        aPayer.setText(String.valueOf(t.getAddition() + t.getPrixBoissons()) + " EUR");
        
        ButtonGroup group = new ButtonGroup();
        group.add(consulterRadioButton);
        group.add(imprimerRadioButton);
        group.add(encaisserRadioButton);
        
        tableEncaisser = t;
    }

